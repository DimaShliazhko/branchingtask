package by.htp.task.cycles;

/*
 * 
 * вывести на экран соответствующий между символами и их численным обозначениями 
 * в памяти компьютера (Таблицу ASCII)
 * 
 */

public class Task26 {
	
	public static void main(String[] args) {
		
	
	char s;
	
	
	
	
	for (int i = 0; i<=255; i++) {
		
		s = (char) i;
		System.out.println(s);
	}
	
	
	
	}
}
	
	


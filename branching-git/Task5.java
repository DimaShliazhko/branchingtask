/*
 * 
 * составить программу определения наименьшего из двух чисел
 */

public class Task5 {
	public static void main(String[] args) {
		
		int a,b;
		a = 8;
		b = 5;
		if (a < b) {
			System.out.println(a +" < " + b+ " число a меньше b");
			
		}
		else {
			System.out.println(b +" < " + a+ " число b меньше a");
		}
	}

}
